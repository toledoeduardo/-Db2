INSERT INTO group_stage (group_name, team_id, matches_played, wins, draws, losses, points)
SELECT 

    CASE 
        WHEN t.id BETWEEN 1 AND 4 THEN 'A'
        WHEN t.id BETWEEN 5 AND 8 THEN 'B'
        WHEN t.id BETWEEN 9 AND 12 THEN 'C'
        WHEN t.id BETWEEN 13 AND 16 THEN 'D'
        WHEN t.id BETWEEN 17 AND 20 THEN 'E'
        WHEN t.id BETWEEN 21 AND 24 THEN 'F'
        WHEN t.id BETWEEN 25 AND 28 THEN 'G'
    END AS group_name,
    t.id AS team_id,
    COUNT(m.id) AS matches_played,
    SUM(CASE 
        WHEN (t.id = m.team1_id AND m.score_team1 > m.score_team2) OR 
             (t.id = m.team2_id AND m.score_team2 > m.score_team1) THEN 1 ELSE 0 
    END) AS wins,
    SUM(CASE 
        WHEN m.score_team1 = m.score_team2 THEN 1 ELSE 0 
    END) AS draws,
    SUM(CASE 
        WHEN (t.id = m.team1_id AND m.score_team1 < m.score_team2) OR 
             (t.id = m.team2_id AND m.score_team2 < m.score_team1) THEN 1 ELSE 0 
    END) AS losses,
    SUM(CASE 
        WHEN (t.id = m.team1_id AND m.score_team1 > m.score_team2) OR 
             (t.id = m.team2_id AND m.score_team2 > m.score_team1) THEN 3 ELSE 0 
    END + CASE 
        WHEN m.score_team1 = m.score_team2 THEN 1 ELSE 0 
    END) AS points
FROM 
    matches m
JOIN 
    teams t ON t.id IN (m.team1_id, m.team2_id)
WHERE 
    m.stage = 'Group Stage' 
GROUP BY 
    group_name, t.id;
